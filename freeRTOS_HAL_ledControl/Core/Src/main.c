void readButtonStateTask(void *params)
{
	while(1)
	{
		if(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0) == GPIO_PIN_SET)
		{
			while(HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0) == GPIO_PIN_SET);

			if(ledConf.count > ms_1000) ledConf.count = ms_100;
			else ledConf.count++;

		}
	}
}

void ledFlashTask(void *params)
{

	while(1)
	{
		switch (ledConf.count)
		{
			case ms_100:
			{
				HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13 | GPIO_PIN_14 | GPIO_PIN_15);
				vTaskDelay(100);
				break;
			}
			case ms_250:
			{
				HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13 | GPIO_PIN_14 | GPIO_PIN_15);
				vTaskDelay(250);
				break;
			}
			case ms_500:
			{
				HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13 | GPIO_PIN_14 | GPIO_PIN_15);
				vTaskDelay(500);
				break;
			}
			case ms_750:
			{
				HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13 | GPIO_PIN_14 | GPIO_PIN_15);
				vTaskDelay(750);
				break;
			}
			case ms_1000:
			{
				HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13 | GPIO_PIN_14 | GPIO_PIN_15);
				vTaskDelay(1000);
				break;
			}
			default:
			{
				HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13 | GPIO_PIN_14 | GPIO_PIN_15, RESET);
				break;
			}
		}

	}
}

